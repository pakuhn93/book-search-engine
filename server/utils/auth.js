const jwt = require('jsonwebtoken');
// using GraphQL's error checking
const { GraphQLError } = require('graphql');

// set token secret and expiration date
const secret = 'mysecretsshhhhh';
const expiration = '2h';

module.exports = {
  // need this to be able to authenticate in GraphQL
  AuthenticationError: new GraphQLError('Unable to authenticate login credentials.', {
    extensions: {
      code: 'UNAUTHENTICATED'
    }
  }),

  // function for our authenticated routes
  authMiddleware: function ({ req }) {
    // allows token to be sent via  req.query or headers
    let token = req.query.token || req.headers.authorization;

    // ["Bearer", "<tokenvalue>"]
    if (req.headers.authorization) {
      token = token.split(' ').pop().trim();
    }

    if (!token) {
      // return res.status(400).json({ message: 'You have no token!' });
      // ^ above code is for RESTful
      return req;
    }

    // verify token and get user data out of it
    try {
      const { data } = jwt.verify(token, secret, { maxAge: expiration });
      req.user = data;
    } catch {
      console.log('Invalid token');
      // return res.status(400).json({ message: 'invalid token!' });
      // ^ don't need the above code because we won't be using a RESTful API anymore
    }

    // send to next endpoint
    // next();
    // ^ above code is for RESTful API, not GraphQL
    return req;
  },
  signToken: function ({ username, email, _id }) {
    const payload = { username, email, _id };

    return jwt.sign({ data: payload }, secret, { expiresIn: expiration });
  },
};
